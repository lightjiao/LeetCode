class Solution
{
public:
    int lengthOfLongestSubstring(string s)
    {
        int       sLength = s.length();
        int       len = 0, i = 0, j = 0;
        set<char> containSet;

        while (i < sLength && j < sLength)
        {
            set<char>::iterator it = containSet.find(s[j]);
            if (it == containSet.end())
            {
                containSet.insert(s[j]);
                j++;
                len = max(len, j - i);
            }
            else
            {
                containSet.erase(s[i]);
                i++;
            }
        }

        return len;
    }
};

