class Solution
{
public:
    /**
     * 滑动窗口
     */
    int lengthOfLongestSubstring(string s)
    {
        unordered_set<char> occ;

        int n = s.size();
        // 右指针初始值为-1，相当于在字符串的左边界的左侧，还没有开始移动
        int rk = -1, ans = 0;

        // 枚举左指针的位置
        for (int i = 0; i < n; i++)
        {
            // 左指针移动后，移除一个字符
            if (i != 0)
            {
                occ.erase(s[i - 1]);
            }

            while (rk + 1 < n && !occ.count(s[rk + 1]))
            {
                // 不断的移动右指针
                occ.insert(s[rk + 1]);
                rk++;
            }

            // 第i 到rk 个字符是一个极长的无重复字符串
            ans = max(ans, rk - i + 1);
        }
        return ans;
    }
};

