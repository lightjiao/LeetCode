class Solution
{
public:
    int lengthOfLongestSubstring(string s)
    {
        int            answer = 0;
        map<char, int> posMap;
        for (int i = 0, j = 0; j < s.length(); j++)
        {
            map<char, int>::iterator it = posMap.find(s[j]);
            if (it != posMap.end())
            {
                i = max(i, it->second + 1);
            }
            posMap[s[j]] = j;
            posMap.insert(pair<char, int>(s[j], j));
            answer = max(answer, j - i + 1);
        }

        return answer;
    }
};

